package HomeWork1;

public class Treadmill {
    private int length;
    private int number;

    public Treadmill(int length, int number) {
        this.length = length;
        this.number = number;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
